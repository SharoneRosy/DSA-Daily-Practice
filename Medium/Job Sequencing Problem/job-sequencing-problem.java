class Solution
{
    //Function to find the maximum profit and the number of jobs done.
    int[] JobScheduling(Job arr[], int n)
    {
        int jobs = 0, res = 0;
        Arrays.sort(arr, (a, b)-> Integer.compare(b.profit,a.profit));
        int maxDl = 0;
        for(Job job : arr){
            maxDl = Math.max(maxDl, job.deadline);
        }
        int[] avDay = new int[maxDl+1];
        Arrays.fill(avDay, -1);
        for(Job job : arr){
            int j = job.deadline;
            while(j > 0 && avDay[j] != -1) j--;
            if(j!=0) {
                avDay[j] = 1;
                jobs++;
                res += job.profit;
            }
        }
        return new int[]{jobs, res};
    }


}

